    public static function __callStatic($name, $arguments) {
        $parts = explode('_', $name, 2);

        $method = $parts[0];
        $source = $parts[1];

        if ($method == 'list' || $method == 'remote') {
            $filter = 'gdcet_data_list_'.$source;
            $property = '_data_'.$source;

            $data = array();
            if (isset(gdcet_predefined_data_meta()->{$property})) {
                $data = gdcet_predefined_data_meta()->{$property};
            }

            $data = apply_filters($filter, $data);
            $input = in_array($source, gdcet_predefined_data_meta()->_numeric_arrays) ? gdcet_predefined_data_meta()->plain_to_associated($data) : $data;

            if ($method == 'list') {
                return $input;
            } else if ($method == 'remote') {
                $search = isset($arguments[0]) ? $arguments[0] : false;

                if ($search === false) {
                    return array();
                }

                if (is_array($search)) {
                    $output = array();

                    foreach ($search as $key) {
                        if (isset($input[$key])) {
                            $output[$key] = $input[$key];
                        }
                    }

                    return $output;
                } else if (is_string($search) || is_numeric($search)) {
                    $page = isset($arguments[1]) ? $arguments[1] : 1;
                    $pager = isset($arguments[2]) ? $arguments[2] : 25;
                    $page = $page > 1 ? $page - 1 : 0;

                    $filtered = array_filter($input, function($val, $key) use ($search) {
                        return strpos($val, $search) !== false || strpos($key, $search) !== false;
                    }, ARRAY_FILTER_USE_BOTH);

                    return array(
                        'total_count' => count($filtered),
                        'filtered' => array_slice($filtered, $page * $pager, $pager)
                    );
                }
            }
        } else if ($method == 'regex') {
            if (isset(gdcet_predefined_data_meta()->regex[$source])) {
                return gdcet_predefined_data_meta()->regex[$source];
            } else {
                return '';
            }
        } else if ($method == 'mask') {
            if (isset(gdcet_predefined_data_meta()->mask[$source])) {
                return gdcet_predefined_data_meta()->mask[$source];
            } else {
                return '';
            }
        }
    }
